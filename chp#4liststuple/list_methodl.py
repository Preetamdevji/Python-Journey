L1 = [1 , 3 , 9 , 5 , 2, 7]
print(L1)

# we can sort the list
L1.sort()
print(L1)

# we can insert the list of the index no
L1.insert(2, 4)
print(L1)

# we can append the list
L1.append(10)
print(L1)

# we can remove the list
L1.remove(10)
print(L1)


# we can reverse the list
L1.reverse()
print(L1)


# we can pop the list
# remove element at the index
L1.pop(2)      
print(L1)



